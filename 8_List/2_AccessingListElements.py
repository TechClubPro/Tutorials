""" Program to Access Individual Elements in the List """

TeamAvengers = ["Shreyas","Geeta","Om","Aditya","Saee"]



#To access the element at index 0 i.e. "Shreyas"
print(TeamAvengers[0])

#To access the element at index 1 i.e. "Geeta"
print(TeamAvengers[1])

#To access the element at index 2 i.e. "Om"
print(TeamAvengers[2])

#To access the element at index 3 i.e. "Aditya"
print(TeamAvengers[3])

#To access the element at index 4 i.e. "Saee"
print(TeamAvengers[4])